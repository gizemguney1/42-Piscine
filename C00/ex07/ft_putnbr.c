#include <unistd.h>

void ft_putnbr(int nb)
{
 char c;
 if(nb == -2147483648)
 {
	write(1, -2147483648, 11);
	return ;
 }
 if (nb < 0)
 {
	write(1, "-", 1);
	nb *= -1;
 }
 if(nb >9)
 ft_putchar(nb / 10);
 c = '0' + (nb %10);
 write(1, &c, 1);
}
int main()
{
	ft_putnbr(-364);
}